''' Strings come under text dataType '''
''' Here We will see how to declare, initialize strings and functions which are important related to strings '''
''' Python string is a sequence of one or more Unicode characters, enclosed in single, 
    double or triple quotation marks (also called inverted commas). 
    Python strings are immutable which means when you perform an operation on strings, 
    you always produce a new string object of the same type, rather than mutating an existing string. '''


str1 = "Hello"
str2 = 'Python'
str3 = '''Coders'''

print(str1, str2, str3)
print(type(str1), type(str2), type(str3))
